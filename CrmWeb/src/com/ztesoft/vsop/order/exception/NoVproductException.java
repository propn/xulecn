package com.ztesoft.vsop.order.exception;

public class NoVproductException extends Exception {

	public NoVproductException() {
		super();
	}

	public NoVproductException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoVproductException(String arg0) {
		super(arg0);
	}

	public NoVproductException(Throwable arg0) {
		super(arg0);
	}

}
