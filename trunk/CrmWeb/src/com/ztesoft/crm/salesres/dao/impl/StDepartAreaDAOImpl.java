    public StDepartAreaVO findByPrimaryKey(String pDEPART_ID)
        throws DAOSystemException {
        ArrayList arrayList = findBySql(SQL_SELECT + " WHERE DEPART_ID = ? ",
                new String[] { pDEPART_ID });

        if (arrayList.size() > 0) {
            return (StDepartAreaVO) arrayList.get(0);
        } else {
            return (StDepartAreaVO) getEmptyVO();
        }
    }

    public ArrayList findBySql(String sql, String[] sqlParams)
        throws DAOSystemException {
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;

        try {
            conn = ConnectionContext.getContext()
                                    .getConnection(JNDINames.CRM_RCDB);
            stmt = conn.prepareStatement(DAOSQLUtils.getFilterSQL(sql));
            stmt.setMaxRows(maxRows);

            for (int i = 0; (sqlParams != null) && (i < sqlParams.length);
                    i++) {
                stmt.setString(i + 1, sqlParams[i]);
            }

            rs = stmt.executeQuery();

            return fetchMultiResults(rs);
        } catch (SQLException se) {
            Debug.print(sql, this);
            throw new DAOSystemException("SQLException while getting sql:\n" +
                sql, se);
        } finally {
            DAOUtils.closeResultSet(rs, this);
            DAOUtils.closeStatement(stmt, this);
            DAOUtils.closeConnection(conn, this);
        }
    }

    protected ArrayList fetchMultiResults(ResultSet rs)
        throws SQLException {
        ArrayList resultList = new ArrayList();

        while (rs.next()) {
            StDepartAreaVO vo = new StDepartAreaVO();
            populateDto(vo, rs);
            resultList.add(vo);
        }

        return resultList;
    }

    protected void populateDto(StDepartAreaVO vo, ResultSet rs)
        throws SQLException {
        vo.setDepartId(DAOUtils.trimStr(rs.getString("DEPART_ID")));
        vo.setDepartCode(DAOUtils.trimStr(rs.getString("DEPART_CODE")));
        vo.setDepartName(DAOUtils.trimStr(rs.getString("DEPART_NAME")));
    }

    public VO populateCurrRecord(ResultSet rs) throws DAOSystemException {
        StDepartAreaVO vo = new StDepartAreaVO();

        try {
            populateDto(vo, rs);
        } catch (SQLException se) {
            Debug.print("populateCurrRecord³ö´í", this);
            throw new DAOSystemException("SQLException while populateDto:\n", se);
        }

        return vo;
    }

