package com.ztesoft.vsop.order.exception;

public class ProductHasNoPlatformException extends Exception {

	public ProductHasNoPlatformException() {
		super();
	}

	public ProductHasNoPlatformException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ProductHasNoPlatformException(String arg0) {
		super(arg0);
	}

	public ProductHasNoPlatformException(Throwable arg0) {
		super(arg0);
	}
	
}
